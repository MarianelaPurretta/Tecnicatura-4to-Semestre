# Profundizando en los sistemas de numeracion

#Sistema decimal
a = 10
print(f"a decimal: {a}")

#Sistema binario
a = 0b1010
print(f"a binario: {a}")

#Sistema octal
a = 0o12
print(f"a octal: {a}")

#Sistema hexadecimal
a = 0xA
print(f"a hexadecimal: {a}")

#Base Decimal
a = int('23', 10)
print(f"a = base decimal: {a}")

#Base binario
a = int('10111', 2)
print(f"a = base binario: {a}")